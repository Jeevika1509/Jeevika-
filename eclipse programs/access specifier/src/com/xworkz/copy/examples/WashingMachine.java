package com.xworkz.copy.examples;

public class WashingMachine {

	public String brand="samsung";
	public String type="top load";
	public String color="silver";
	public double loadCapacity=6.5;
	public int price=15000;
	public int modelNo=65;
	public int quantity=1;
	public int warranty=1;
	public boolean offers=true;
	public boolean working=true;
	public String material="steel";
	public String getBrand() {
		return brand;
	}
	 void setBrand(String brand) {
		this.brand = brand;
	}
	public String getType() {
		return type;
	}
	 void setType(String type) {
		this.type = type;
	}
	public String getColor() {
		return color;
	}
	 void setColor(String color) {
		this.color = color;
	}
	public double getLoadCapacity() {
		return loadCapacity;
	}
	 void setLoadCapacity(double loadCapacity) {
		this.loadCapacity = loadCapacity;
	}
	public int getPrice() {
		return price;
	}
	 void setPrice(int price) {
		this.price = price;
	}
	public int getModelNo() {
		return modelNo;
	}
	 void setModelNo(int modelNo) {
		this.modelNo = modelNo;
	}
	public int getQuantity() {
		return quantity;
	}
	 void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public int getWarranty() {
		return warranty;
	}
	 void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	public boolean isOffers() {
		return offers;
	}
	 void setOffers(boolean offers) {
		this.offers = offers;
	}
	public boolean isWorking() {
		return working;
	}
	 void setWorking(boolean working) {
		this.working = working;
	}
	public String getMaterial() {
		return material;
	}
	 void setMaterial(String material) {
		this.material = material;
	}
	
	
}
