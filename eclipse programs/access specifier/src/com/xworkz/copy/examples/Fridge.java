package com.xworkz.copy.examples;

public class Fridge {
	

	private String brand="whirlpool";
	private String color="grey";
	private int numberOfDoors=1;
	private int modelNumber=342;
	public double length=260;
	public double width=67;
	public double weight=57;
	public double capacity=56;
	double price=15000;
	String offers="5%";
	int warranty=1;
	
	public String getBrand() {
		return brand;
	}
	 void setBrand(String brand) {
		this.brand = brand;
	}
	public String getColor() {
		return color;
	}
	 void setColor(String color) {
		this.color = color;
	}
	public int getNumberOfDoors() {
		return numberOfDoors;
	}
	 void setNumberOfDoors(int numberOfDoors) {
		this.numberOfDoors = numberOfDoors;
	}
	public int getModelNumber() {
		return modelNumber;
	}
	 void setModelNumber(int modelNumber) {
		this.modelNumber = modelNumber;
	}
	public double getLength() {
		return length;
	}
	 void setLength(double length) {
		this.length = length;
	}
	public double getWidth() {
		return width;
	}
	 void setWidth(double width) {
		this.width = width;
	}
	public double getWeight() {
		return weight;
	}
	 void setWeight(double weight) {
		this.weight = weight;
	}
	public double getCapacity() {
		return capacity;
	}
	 void setCapacity(double capacity) {
		this.capacity = capacity;
	}
	public double getPrice() {
		return price;
	}
	 void setPrice(double price) {
		this.price = price;
	}
	public String getOffers() {
		return offers;
	}
	 void setOffers(String offers) {
		this.offers = offers;
	}
	public int getWarranty() {
		return warranty;
	}
	 void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	
	

}
