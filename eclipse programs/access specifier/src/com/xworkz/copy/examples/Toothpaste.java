package com.xworkz.copy.examples;

public class Toothpaste {

	private String brand="colgate";
	private String flavour="minty";
	private double weight=240;
	private int warranty=2;
	public String color="red";
	public int price=200;
	public int barcode=28382;
	String manufacturingDate="18 nov 2022";
	String expiryDate="18 nov 2024";
	String offers="12 %";
	int quantity=1;
	
	public String getBrand() {
		return brand;
	}
	 void setBrand(String brand) {
		this.brand = brand;
	}
	public String getFlavour() {
		return flavour;
	}
	 void setFlavour(String flavour) {
		this.flavour = flavour;
	}
	public double getWeight() {
		return weight;
	}
	 void setWeight(double weight) {
		this.weight = weight;
	}
	public int getWarranty() {
		return warranty;
	}
	 void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	public String getColor() {
		return color;
	}
	 void setColor(String color) {
		this.color = color;
	}
	public int getPrice() {
		return price;
	}
	 void setPrice(int price) {
		this.price = price;
	}
	public int getBarcode() {
		return barcode;
	}
	 void setBarcode(int barcode) {
		this.barcode = barcode;
	}
	public String getManufacturingDate() {
		return manufacturingDate;
	}
	 void setManufacturingDate(String manufacturingDate) {
		this.manufacturingDate = manufacturingDate;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	 void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getOffers() {
		return offers;
	}
	 void setOffers(String offers) {
		this.offers = offers;
	}
	public int getQuantity() {
		return quantity;
	}
	 void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	
}
