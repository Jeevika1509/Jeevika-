package com.xworkz.copy.examples;

public class Bag {
	
	public String brand="wildcraft";
	public String color="green";
	public double price=1500;
	public int warranty=1;
	public boolean adjustable=true;
	public boolean laptopCompatible=false;
	public String material="polyester";
	public boolean waterResistant=false;
	public double capacity=1;
	public String offers="5%";
	public int quantity=1;
	
	public String getBrand() {
		return brand;
	}
	void setBrand(String brand) {
		this.brand = brand;
	}
	public String getColor() {
		return color;
	}
	void setColor(String color) {
		this.color = color;
	}
	public double getPrice() {
		return price;
	}
	void setPrice(double price) {
		this.price = price;
	}
	public int getWarranty() {
		return warranty;
	}
	void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	public boolean isAdjustable() {
		return adjustable;
	}
	void setAdjustable(boolean adjustable) {
		this.adjustable = adjustable;
	}
	public boolean isLaptopCompatible() {
		return laptopCompatible;
	}
	void setLaptopCompatible(boolean laptopCompatible) {
		this.laptopCompatible = laptopCompatible;
	}
	public String getMaterial() {
		return material;
	}
	void setMaterial(String material) {
		this.material = material;
	}
	public boolean isWaterResistant() {
		return waterResistant;
	}
	void setWaterResistant(boolean waterResistant) {
		this.waterResistant = waterResistant;
	}
	public double getCapacity() {
		return capacity;
	}
	void setCapacity(double capacity) {
		this.capacity = capacity;
	}
	public String getOffers() {
		return offers;
	}
	void setOffers(String offers) {
		this.offers = offers;
	}
	public int getQuantity() {
		return quantity;
	}
	void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	

}
