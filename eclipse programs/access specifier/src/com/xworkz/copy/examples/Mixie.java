package com.xworkz.copy.examples;

public class Mixie {

	public String brand="butterfly";
	public int modelNumber=655;
	public String color="grey";
	public double length=18;
	public double width=51;
	public double capacity=1.25;
	public double speed=20000;
	public int warranty=1;
	public int price=3000;
	public boolean working=true;
	public String material="stainless steel";
	
	
	public String getBrand() {
		return brand;
	}
	 void setBrand(String brand) {
		this.brand = brand;
	}
	public int getModelNumber() {
		return modelNumber;
	}
	 void setModelNumber(int modelNumber) {
		this.modelNumber = modelNumber;
	}
	public String getColor() {
		return color;
	}
	 void setColor(String color) {
		this.color = color;
	}
	public double getLength() {
		return length;
	}
	 void setLength(double length) {
		this.length = length;
	}
	public double getWidth() {
		return width;
	}
	 void setWidth(double width) {
		this.width = width;
	}
	public double getCapacity() {
		return capacity;
	}
	 void setCapacity(double capacity) {
		this.capacity = capacity;
	}
	public double getSpeed() {
		return speed;
	}
	 void setSpeed(double speed) {
		this.speed = speed;
	}
	public int getWarranty() {
		return warranty;
	}
	 void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	public int getPrice() {
		return price;
	}
	 void setPrice(int price) {
		this.price = price;
	}
	public boolean isWorking() {
		return working;
	}
	 void setWorking(boolean working) {
		this.working = working;
	}
	public String getMaterial() {
		return material;
	}
	 void setMaterial(String material) {
		this.material = material;
	}
	
	

	
}
