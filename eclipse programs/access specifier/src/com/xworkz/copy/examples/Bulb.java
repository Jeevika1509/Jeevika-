package com.xworkz.copy.examples;

public class Bulb {
	
	private String brand="syska";
	private String color="white";
	private String modelno="B22";
	private String type="led";
	public int price=300;
	public int warranty=1;
	public boolean working=true;
	public int quantity=1;
	double weight=100;
	String material="alluminium";
	boolean offers=true;
	
	
	public String getBrand() {
		return brand;
	}
	 void setBrand(String brand) {
		this.brand = brand;
	}
	public String getColor() {
		return color;
	}
	 void setColor(String color) {
		this.color = color;
	}
	public String getModelno() {
		return modelno;
	}
	 void setModelno(String modelno) {
		this.modelno = modelno;
	}
	public String getType() {
		return type;
	}
	 void setType(String type) {
		this.type = type;
	}
	public int getPrice() {
		return price;
	}
	 void setPrice(int price) {
		this.price = price;
	}
	public int getWarranty() {
		return warranty;
	}
	 void setWarranty(int warranty) {
		this.warranty = warranty;
	}
	public boolean isWorking() {
		return working;
	}
	 void setWorking(boolean working) {
		this.working = working;
	}
	public int getQuantity() {
		return quantity;
	}
	 void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public double getWeight() {
		return weight;
	}
	 void setWeight(double weight) {
		this.weight = weight;
	}
	public String getMaterial() {
		return material;
	}
	 void setMaterial(String material) {
		this.material = material;
	}
	public boolean isOffers() {
		return offers;
	}
	 void setOffers(boolean offers) {
		this.offers = offers;
	}
	

}
